public class DataTypeInspector{
    public static void main(String[]args){
        byte byteval = 10;
        short shortVal = 2000;
        int intVal = 50000;
        long longVal = 15000000000L;
        float floatVal = 5.75f;
        double doubleVal = 19.99;
        char charVal = 'A';
        boolean booleanVal = true;
        System.out.println("\n");
        System.out.println("Java Primitive Data Types:");
        System.out.println("Byte value: " + byteval);
        System.out.println("Short value: " + shortVal);
        System.out.println("Int value: " + intVal);
        System.out.println("Long value: " + longVal);
        System.out.println("Float value: " + floatVal);
        System.out.println("Double value: " + doubleVal);
        System.out.println("Char value: " + charVal);
        System.out.println("Boolean value: " + booleanVal);
            
    }
}